# Estrutura de dados: Lista e Dicionário

# Lista com nomes de alunos
alunos = ["Alice", "Bruno", "Carlos"]

# Dicionário com idades
idades = {
    "Alice": 15,
    "Bruno": 16,
    "Carlos": 17
}

# Exibindo os nomes e idades
for aluno in alunos:
    print(aluno + " tem " + str(idades[aluno]) + " anos.")
